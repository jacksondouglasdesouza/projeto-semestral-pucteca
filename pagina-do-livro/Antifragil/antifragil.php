          <h1>
            O Antifrágil
          </h1>
          <h2>
            Do autor dos best-sellers A lógica do Cisne Negro e Arriscando a própria pele, Antifrágil revela como prosperar em um mundo de incertezas. Em nova tradução.
          </h2>
          <p>

            Em A lógica do Cisne Negro, Taleb demonstrou que acontecimentos altamente improváveis e imprevisíveis dominam a nossa existência. Em Antifrágil, o autor confere um novo conceito à incerteza, tornando-a desejável e até mesmo necessária. Assim como ficamos fisicamente mais fortes quando submetidos à tensão, muitas coisas se beneficiam do estresse, da desordem e da volatilidade. O que Taleb identificou e chama de antifrágil não só tira proveito do caos, como precisa dele para sobreviver e florescer. O antifrágil está além do resiliente e do robusto. Estes resistem a choques e permanecem os mesmos; o antifrágil, por sua vez, se torna cada vez melhor. Além disso, ele é imune a erros de previsão e está protegido de eventos adversos.
            Extremamente ambicioso e multidisciplinar, Antifrágil é sobre como se comportar — e prosperar — em um mundo cheio de imprevistos. Erudita e espirituosa, a mensagem de Taleb é revolucionária: o que não é antifrágil certamente sucumbirá.
            "Mudou a minha visão de como o mundo funciona." — Daniel Kahneman, prêmio Nobel de economia e autor de Rápido e devagar
            "Ambicioso e provocador... Extremamente divertido." — The Economist
            "A voz mais profética de todos os tempos. Taleb é um filósofo genuinamente importante. Alguém capaz de mudar a maneira como enxergamos a estrutura do mundo somente com a força, originalidade e veracidade de suas ideias." — GQ
            "Modificou o pensamento moderno." — The Times (Londres)
